#!/usr/bin/env node
// Generate the WebGL2 static-import shim for the wasm32-wasi build.
//
// love-wasi keeps upstream glad verbatim and only reseams its *loader*: instead
// of fetching GL function pointers from SDL_GL_GetProcAddress, glad's
// gladLoadGLLoader() populates its fp_* table from a callback that returns the
// address of a static wasm import -- the host's WebGL2 context IS the GL surface
// (readme.md, "GL function loading"). This generator emits that callback plus
// one import declaration per GL entry point the opengl backend actually calls,
// with the signature taken from glad's own inline wrapper (so glad's pfn_* cast
// is ABI-correct). Entry points glad queries but WebGL2 lacks resolve to null,
// which glad reports as an absent feature -- the faithful GLES 3.0 profile.
//
//   node wasi/graphics/gen-gl-imports.mjs > wasi/graphics/gl-imports.cpp
//
// Re-run when the backend's GL call set changes; the output is committed so the
// diff stays the evidence (and the build needs no generator at compile time).
import { readFileSync, readdirSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(HERE, "..", "..");
const BACKEND = path.join(ROOT, "src/modules/graphics/opengl");
const GLADFUNCS = path.join(ROOT, "src/libraries/glad/gladfuncs.hpp");

// 1. The GL entry points the backend references (member vars like glMode and
//    comment mentions are filtered out below by requiring a real glad wrapper).
const called = new Set();
const callRe = /\bgl[A-Z][A-Za-z0-9]+\b/g;
for (const f of readdirSync(BACKEND).filter((f) => f.endsWith(".cpp")).sort()) {
  const text = readFileSync(path.join(BACKEND, f), "utf8");
  for (const m of text.matchAll(callRe)) called.add(m[0]);
}

// 2. glad's callable interface: name -> (return type, params), single-line inline.
const wrappers = new Map();
const wpat = /^inline\s+(.+?)\s+(gl\w+)\((.*?)\)\s*\{/;
for (const line of readFileSync(GLADFUNCS, "utf8").split("\n")) {
  const m = wpat.exec(line.trim());
  if (m) wrappers.set(m[2], [m[1].trim(), m[3].trim()]);
}

const got = [...called].filter((n) => wrappers.has(n)).sort();
const skipped = [...called].filter((n) => !wrappers.has(n)).sort();
process.stderr.write(
  `// ${got.length} imports generated; ignored non-calls: ${skipped.join(" ")}\n`
);

const L = [];
L.push(`/**
 * WebGL2 GL entry-point import shim for the wasm32-wasi (browser) build.
 *
 * GENERATED by wasi/graphics/gen-gl-imports.mjs from src/libraries/glad/gladfuncs.hpp
 * — do not edit by hand; re-run the generator if the backend's GL call set changes.
 *
 * love-wasi replaces glad's runtime GL loader (which fetches function pointers
 * from SDL_GL_GetProcAddress) with static wasm imports: the host's WebGL2
 * context IS the GL surface (readme.md, "GL function loading"). We do NOT
 * replace glad itself — upstream glad.cpp is compiled verbatim and its
 * gladLoadGLLoader() populates the fp_* table from the loader callback below,
 * so every glad header and every backend call site stays byte-unchanged. Only
 * the loader source changes: SDL proc-address -> address of a static import.
 *
 * Each import is declared with glad's own wrapper signature (so the fp_* cast
 * in gladLoadGLLoader is ABI-correct). Names glad queries but we don't provide
 * resolve to null, which glad reports as an absent entry point / feature — the
 * faithful WebGL2 (GLES 3.0) capability profile, host-reported.
 */
#include "libraries/glad/glad.hpp"
#include <cstring>

using namespace glad;

#define WGL_IMPORT(sym) __attribute__((import_module("love_gl"), import_name(sym)))

extern "C" {
`);

for (const n of got) {
  const [ret, params] = wrappers.get(n);
  L.push(`WGL_IMPORT("${n}") ${ret} wgl_${n}(${params});`);
}

L.push(`}

namespace love {
namespace graphics {
namespace opengl {

// The GL proc-address hook the OpenGL backend calls under
// LOVE_GRAPHICS_GL_STATIC_IMPORTS (its guarded loader seam) in place of
// SDL_GL_GetProcAddress. Returns the address of the matching static import, or
// null for anything the WebGL2 host doesn't provide (glad then leaves that fp_*
// null / its GLAD flag false — the faithful GLES 3.0 profile).
void *getStaticGLProcAddress(const char *name)
{
\tstatic const struct { const char *name; void *fn; } table[] = {`);

for (const n of got) {
  L.push(`\t\t{"${n}", (void *)&wgl_${n}},`);
}

L.push(`\t};
\tfor (const auto &e : table)
\t\tif (std::strcmp(e.name, name) == 0)
\t\t\treturn e.fn;
\treturn nullptr;
}

} // opengl
} // graphics
} // love`);

process.stdout.write(L.join("\n") + "\n");
